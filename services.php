        <p>
            Plaque is the bacteria that your dental hygienist will remove from your teeth during your dental hygiene
            appointment. It is a film bacterium that forms on your teeth and gums after eating foods that produce acids.
            These foods may include carbohydrates (sugars and starches) such as candy, cookies, bread, crackers, and cereal.
        </p>

        <p>
            When plaque remains on your teeth for an extended period of time the tooth may start to decay which can
            lead to cavities, allowing the bacteria to ‘eat away’ at the surfaces of your teeth and gums.
            Plaque can also lead to gum irritation, soreness, and redness. Sometimes your gums may begin to bleed.
            This gradual degeneration can often cause gums to pull away from teeth. This condition is called receding gums.
            Long-term plaque can lead to serious problems. The bacteria can also form pockets of disease around tooth
            structures that will eventually destroy the bone beneath the tooth. This is why it is important to see the
            dental hygienist on a regular basis.
        </p>
