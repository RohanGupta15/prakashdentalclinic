                                Q.5 What can dental bonding do for my teeth?
                            </h4>
                        </div>
                        <div id="q5" class=" card-body collapse text-muted">
                            <p class="answer">
                                A. Dental bonding is a simple, painless and affordable procedure that has many uses including:<br />
                                <ul>
                                    <li>Repairing chipped or cracked teeth</li>
                                    <li>Closing spaces between teeth</li>
                                    <li>Reshaping teeth</li>
                                    <li>Covering badly stained teeth</li>
                                </ul>
                                Bonding involves the application of a tooth-colored resin.
                                The dentist prepares the tooth for bonding by placing a mild solution on it to roughen the surface.
                                Using a shade guide, the dentist selects a shade that closely matches your tooth and then mixes and tints the resin.
                                The resin is applied in thin layers and then hardened with a LED or Halogen light.
                                The dentist then shapes and polishes the resin to smooth it and give it a natural look.
                                <br /><br />
                                The procedure usually takes about 30-60 minutes to complete.
                                You can even get several teeth bonded in one visit.
                                No special techniques are needed to care for bonded teeth – just maintain good oral hygiene.
                            </p>
