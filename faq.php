                                Oral infections are a serious health matter. Symptoms include tooth,
                                gum and jaw pain, lumps or pus-filled abscesses and weeping or bleeding gums.
                                Left untreated, they can affect your overall health.<br /><br />
                                Your dentist can diagnose an infection during an oral examination.
                                They will feel your gums, gently tap your teeth and take x-rays to determine the extent of the infection
                                and whether it has spread.<br /><br />
                                If your dentist feels the infection is harmful or your body is not fighting it effectively
                                they may prescribe antibiotics.
                                Your dentist will also want to treat the underlying cause of an oral infection to prevent it from reoccurring
                                in the future. Causes may include tooth decay, periodontal disease, tooth cracks and weakened fillings.
                                Do not ignore the signs of oral infection. Practice good oral hygiene on a daily basis and have your mouth and teeth examined by a dentist.
