                                Q.6 What should I do if I lose a tooth?
                            </h4>
                        </div>
                        <div id="q6" class="card-body collapse text-muted">
                            <p class="answer">
                                A. Having a tooth knocked out is one of the most serious dental emergencies.
                                It can be caused by accidental falls, sports-related trauma, car accidents and even by biting into hard food.
                                However, the damage can be fixed. If you act quickly, there is a good chance the tooth can be saved.
                                <br /><br />
                                Immediately contact our office when a tooth is broken or knocked out.
                                If your dentist can put the tooth back in place within an hour,
                                there is a good chance that the supporting tissues will reattach and hold the tooth in place.
                                <br /><br />
                                If you can find the tooth after the accident or injury, bring it with you when you seek medical help.
                                Pick the tooth up by the crown &#8211;
                                this is the part of the tooth that you see in the mouth – and avoid touching the root end.
                                Do not wrap the tooth in tissue, gauze or scrub the tooth, this will damage the delicate cells on the root needed to attach the tooth back to the gum.
                                Place the tooth in a cup of milk (or water, if milk is not available) to keep it moist.
                                Remember: get to a dentist as quickly as possible.
                                The chances of saving the knocked out tooth are much greater if seen within an hour.
                            </p>
