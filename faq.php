                                Q.15 What age will my children loose their teeth?
                            </h4>
                        </div>
                        <div id="q15" class="card-body collapse text-muted">
                            <p class="answer">
                                A. A child&#8217;s tooth (primary teeth) begin to loosen and fall out to make
                                room for permanent teeth at about age 6.
                                <br /><br />
                                The first baby teeth to fall out are typically the two bottom front teeth
                                (lower central incisors) and the two top front teeth (upper central incisors)
                                followed by the lateral incisors, molars and canines. Molars typically fall
                                out between ages 9 and 12 and are replaced by permanent teeth by about age 13.
                                <br /><br />
                                Baby teeth usually stay in place until they are pushed out by permanent teeth.
                                If a child loses a baby tooth early as a result of tooth decay or an accident,
                                the teeth beside it might drift into the empty space. Teeth in the opposite jaw
                                might also move up or down to fill the gap. This can crowd permanent teeth and
                                cause them to come in crooked.
                                <br /><br />
                                Some children are excited to lose their baby teeth, while others are nervous
                                about this childhood milestone. If your child wants you to pull out a loose tooth,
                                grasp it firmly with a tissue or piece of gauze and remove it with a quick twist.
                                If the tooth is resistant wait a few days and try again. If you&#8217;re concerned
                                about a baby tooth that doesn&#8217;t seem to loosen sufficiently on its own,
                                check with your Dentist. He or she might recommend a wait-and-see approach or
                                an extraction in the dental office.
                                <br /><br />
                                When your child start to lose his or her baby teeth, reinforce the importance
                                of proper dental care. With proper care you can help your child&#8217;s
